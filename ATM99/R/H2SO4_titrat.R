function(conc_SO4) {
  td <- data.frame(cSO4 = c(0, 0.3198848, 0.639552, 0.958976, 1.022816, 
                            1.086656, 1.150496, 1.214336, 1.278144, 1.341984, 1.373888, 1.405824, 
                            1.469728, 1.533632, 1.597536, 1.917024, 2.236544), 
                   pH = c(7.1, 6.59832, 6.32369, 6.07563, 6.0033, 5.91661, 5.80837, 5.66415, 5.44727, 
                            4.99774, 4.17553, 3.10349, 2.60552, 2.37891, 2.23069, 1.84239, 
                            1.64111))
  pH <- approx(td$cSO4, td$pH, xout = conc_SO4, rule = 2)$y
  return(pH)
}
